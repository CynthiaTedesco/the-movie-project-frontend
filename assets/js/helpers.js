export function sanitizedId(attr, category) {
  let sanitizedCategory;
  if (category[0] === ".") {
    sanitizedCategory = category
      .substr(1)
      .split(" ")
      .join("-")
      .split(".")
      .join("--");

    sanitizedCategory = `.${sanitizedCategory}`;
  } else {
    sanitizedCategory = category
      .split(" ")
      .join("-")
      .split(".")
      .join("--");
  }
  return `#${attr} ${sanitizedCategory}`;
}

